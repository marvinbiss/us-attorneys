/**
 * Get Hispanic cities filtered by minimum Hispanic population percentage.
 * Returns sorted by percentage descending.
 */
export function getHispanicCities(minPct?: number): HispanicCity[] {
  const threshold = minPct ?? 0
  return HISPANIC_CITIES
    .filter(c => c.hispanicPopulationPct >= threshold)
    .sort((a, b) => b.hispanicPopulationPct - a.hispanicPopulationPct)
}
