// HELPER FUNCTIONS
// ==========================================================================

// Indexes for O(1) lookups
const _bySlug = new Map<string, PracticeArea>()
const _bySpanishSlug = new Map<string, PracticeArea>()
const _byCategory = new Map<string, PracticeArea[]>()
const _byParent = new Map<string, PracticeArea[]>()

// Build indexes once
for (const pa of PRACTICE_AREAS_200) {
  _bySlug.set(pa.slug, pa)
  _bySpanishSlug.set(pa.spanishSlug, pa)

  if (!_byCategory.has(pa.category)) {
    _byCategory.set(pa.category, [])
  }
  _byCategory.get(pa.category)!.push(pa)

  if (pa.parentSlug) {
    if (!_byParent.has(pa.parentSlug)) {
      _byParent.set(pa.parentSlug, [])
    }
    _byParent.get(pa.parentSlug)!.push(pa)
  }
}

/** Get a practice area by its URL slug */
export function getPracticeAreaBySlug(slug: string): PracticeArea | undefined {
  return _bySlug.get(slug)
}

/** Get all practice areas in a category */
export function getPracticeAreasByCategory(category: string): PracticeArea[] {
  return _byCategory.get(category) ?? []
}

/** Get subspecialties (children) of a given parent slug */
export function getSubspecialties(parentSlug: string): PracticeArea[] {
  return _byParent.get(parentSlug) ?? []
}

/** Get all top-level practice areas (no parent) */
export function getTopLevelPracticeAreas(): PracticeArea[] {
  return PRACTICE_AREAS_200.filter(pa => pa.parentSlug === null)
}

/** Get all slugs (useful for generateStaticParams) */
export function getAllPracticeAreaSlugs(): string[] {
  return PRACTICE_AREAS_200.map(pa => pa.slug)
}

/** Get the Spanish slug for a given English slug */
export function getSpanishSlug(englishSlug: string): string | undefined {
  return _bySlug.get(englishSlug)?.spanishSlug
}

/** Get the English slug for a given Spanish slug */
export function getEnglishSlug(spanishSlug: string): string | undefined {
  return _bySpanishSlug.get(spanishSlug)?.slug
}

/** Get all practice areas with high search volume (priority for page generation) */
export function getHighVolumePracticeAreas(): PracticeArea[] {
  return PRACTICE_AREAS_200.filter(pa => pa.searchVolume === 'high')
}
