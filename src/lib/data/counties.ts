// Lazy-parsed county objects
let _counties: County[] | null = null
let _slugIndex: Map<string, County> | null = null
let _fipsIndex: Map<string, County> | null = null
let _stateIndex: Map<string, County[]> | null = null

function toSlug(name: string, stateCode: string): string {
  return name
    .toLowerCase()
    .replace(/\./g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '') + '-' + stateCode.toLowerCase()
}

function getAllCounties(): County[] {
  if (!_counties) {
    _counties = COUNTIES_RAW.map(([fips, name, stateCode, population, seat]) => ({
      fips,
      name,
      stateCode,
      slug: toSlug(name, stateCode),
      population,
      seat,
    }))
  }
  return _counties
}

function getSlugIndex(): Map<string, County> {
  if (!_slugIndex) {
    _slugIndex = new Map()
    for (const c of getAllCounties()) {
      _slugIndex.set(c.slug, c)
    }
  }
  return _slugIndex
}

function getFipsIndex(): Map<string, County> {
  if (!_fipsIndex) {
    _fipsIndex = new Map()
    for (const c of getAllCounties()) {
      _fipsIndex.set(c.fips, c)
    }
  }
  return _fipsIndex
}

function getStateIndex(): Map<string, County[]> {
  if (!_stateIndex) {
    _stateIndex = new Map()
    for (const c of getAllCounties()) {
      const arr = _stateIndex.get(c.stateCode) ?? []
      arr.push(c)
      _stateIndex.set(c.stateCode, arr)
    }
  }
  return _stateIndex
}

/** Look up a county by its URL-safe slug (e.g. 'los-angeles-ca') */
export function getCountyBySlug(slug: string): County | undefined {
  return getSlugIndex().get(slug)
}

/** Get all counties for a given 2-letter state code */
export function getCountiesByState(stateCode: string): County[] {
  return getStateIndex().get(stateCode.toUpperCase()) ?? []
}

/** Get the top N counties sorted by population descending */
export function getTopCounties(limit: number): County[] {
  return [...getAllCounties()]
    .sort((a, b) => b.population - a.population)
    .slice(0, limit)
}

/** Look up a county by its 5-digit FIPS code */
export function getCountyByFips(fips: string): County | undefined {
  return getFipsIndex().get(fips)
}

/** Total number of counties in the dataset */
export function getCountyCount(): number {
  return COUNTIES_RAW.length
}
